    public boolean isEmail(){
        String o = "^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$";
       
        if(Pattern.matches(o,this.textEmail.getText())){
            return true;
        }else{
            return false;
        }
    }
