    private void buttonAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAceptarMouseClicked
        // Botón para dar de alta al jugador.
        
        String[] msg = {"Datos incompletos","ID, telefono, salario o email erroneo","Introduce correctamente el email, salario, telefono o el numero del jugador"};
        int msgIndice=1;
        boolean crearJugador=true; //ture-> Crea estadio, false-> No crea estadio,        
        //Elementos del jugador
        int idJugador = 0;
        int num = 0;
        Double salario = 0.0;
        String telefono = "";
        String nombre = "";
        String email = "";
        String posicion = "";
        Boolean titular = false;
        
        //
        if(this.textName.getText().equals("") || this.textEmail.getText().equals("") || 
                this.textIdJugador.getText().equals("") || this.textSalario.getText().equals("") ||
                this.textTelefono.getText().equals("")  || this.textNumJugador.getText().equals("")){
            //Algún campo est vacío.
            
            msgIndice=0;
            crearJugador=false;
        }else{
            try{
                idJugador = Integer.parseInt(this.textIdJugador.getText());
                num = Integer.parseInt(this.textNumJugador.getText());
                salario = Double.parseDouble(this.textSalario.getText());
                
            }catch(Exception e){//Introdujo un carracter
                msgIndice=2;
                crearJugador=false;
            }
            
            titular = this.buttunTitular.isSelected();
            posicion = AppFutbol.posicionesJugador[ this.selectPosicion.getSelectedIndex() ];
            email = this.textEmail.getText();
            telefono = this.textTelefono.getText();
            if(!( isEmail() )|| !( isTele() )){//Si el email no tiene la extructura x@x.x
                
                msgIndice=2;
                crearJugador = false;
            }else{//Si cumple la estrucutra el email
                
                nombre = this.textName.getText();

                //Comprobamos que los datos sean correctos
                if(idJugador<0 || salario<=0 || num <=0 ){//El identificador debe ser mayor o igual que 0.
                    msgIndice=1;
                    crearJugador = false;
                }else{
                    //
                   
                    if(!AppFutbol.lJugadores.isEmpty()){//Lista no vacía
                        //TODO ----------- Comprobar que el numero del jugador no este escogido.
                        if(AppFutbol.lJugadores.containsKey(idJugador)){
                            //El id del estadio ya existe en la lista de estadios.
                            int y= JOptionPane.showConfirmDialog(null, "Id del jugador ya en uso, ¿Modificar estadio de la lista? ",
                                "AppFutbol",JOptionPane.YES_NO_OPTION);
                            if(y==JOptionPane.NO_OPTION){//Dijo que no queria borrar el estadio ya existente.
                                crearJugador=false;
                                msgIndice=-1;
                            }

                        }//Fin del fi, para ver si el identificador esta en uso
                    }//Fin del if, para ver si esta la lista vacia.

                }//Fin del if-else que comprueba el IdJugador, el salario y el telefono
            }//Fin del if-else que compruabe la validez del e-mail
        }//Fin del if-else que comprueba los datos vacíos
        
        if(!crearJugador){
            if(msgIndice!=-1){//Si el error no procede del id ya en uso.
                JOptionPane.showMessageDialog(null, msg[msgIndice],
                    "AppFutbol",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            
            Personas p = new Personas(idJugador,nombre,email,telefono);
            j = new Jugador(p, salario,num,posicion,titular);
            AppFutbol.AltaJugador(j);
            
            
            this.setVisible(false);
            ventana.setVisible(true);
        }  
        
    }//GEN-LAST:event_buttonAceptarMouseClicked

